ecs_loader_system::ecs_loader_system()
{
}

ecs_loader_system::~ecs_loader_system()
{
}

/*=============================================================================
PUBLIC METHODS
=============================================================================*/

void ecs_loader_system::run(ecs& ecs, gpu& gpu)
{
	auto& loader = ecs.loader_singleton;

	for (const auto& model_file : loader.models)
	{
		gpu.load_model(model_file);
	}
}

